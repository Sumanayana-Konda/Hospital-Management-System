 private void TableFilling() {
    
          
          DefaultTableModel model = (DefaultTableModel) labManDir.getModel();
          model.setRowCount(0);  
          for(Lab_Manager en : ecoSystem.getLabmanagerdir().getLabManagerList())             
            {
                            
            Object[] row = new Object[5];
         
          
               row[0] = en.getFirstName()+" "+en.getLastName();
               row[1] = en.getDateOfBirth();
               row[2] = en.getEmailId();
               row[3] = en.getGender();
               row[4] = en.getPhoneNumber();
               
              
            model.addRow(row);
            } 
    
    }
