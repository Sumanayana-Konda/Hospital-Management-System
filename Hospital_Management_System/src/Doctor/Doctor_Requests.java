    private void populatetable() {
    
         SimpleDateFormat dateFormat = new SimpleDateFormat("MMM dd, yyyy");
        DefaultTableModel model = (DefaultTableModel) req.getModel();
        model.setRowCount(0);
        for (OperationRequest pObj : ecoSystem.getOperationsbookingdir().getOpRequestList()) {
            if(d.getEmailId().equals(pObj.getDoctor().getEmailId())){
            Object[] row = new Object[5];
            row[0] = pObj.getPatient().getFirstName()+" "+pObj.getPatient().getLastName();
            row[1] = pObj.getPatient().getEmailId();
            row[2] = dateFormat.format(pObj.getDateofOperation());
            row[3] = pObj.getInference();
            row[4] = pObj.getStatus();
            model.addRow(row);
        }
        }
    }
    
    
