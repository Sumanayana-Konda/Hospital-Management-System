    private void jViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jViewActionPerformed
        // TODO add your handling code here:
        
              
        
        int selectedRow = JInsurance_req.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please Select a row");
            return;
        }
           
           
             for(Insurance_Request ir : ecoSystem.getInsurancereqdir().getInsuranceReqList())             
            {
                            
            
            kind.setSelectedItem(ir.getInsurancename());
            jname.setText(ir.getPatient().getFirstName()+" "+ir.getPatient().getLastName());
            jemail.setText(ir.getPatient().getEmailId());
            jincome.setText(String.valueOf(ir.getIncome()));
            jemp.setText(ir.getEmployment());

            if((Integer.valueOf((jincome.getText()))>=100000)||(jemp.getText().equals("Government Worker"))){
            jTxt.setText("The System Recommendation is to Approve this request.");}
            else{
            jTxt.setText("The System Recommendation is to Reject this request.");
            }}
          
    }//GEN-LAST:event_jViewActionPerformed
    
public void populate(){
 DefaultTableModel model = (DefaultTableModel) JInsurance_req.getModel();
          model.setRowCount(0);  
          for(Insurance_Request en : ecoSystem.getInsurancereqdir().getInsuranceReqList())             
            {
                            
            Object[] row = new Object[6];
               
               row[0] = en;
               row[1] = en.getPatient().getFirstName()+" "+en.getPatient().getLastName();
               row[2] = en.getPatient().getEmailId();
               row[3] = en.getIncome();
               row[4] = en.getEmployment();
               row[5] = en.getStatus();
              
            model.addRow(row);
            }

}    
